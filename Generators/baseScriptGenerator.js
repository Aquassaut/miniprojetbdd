function createBase() {
    console.log(
        'drop table if exists contenu;\n' +
        'drop table if exists participe;\n' +
        'drop table if exists etudiant;\n' +
        'drop table if exists manifestation;\n' +
        'drop table if exists epreuve;\n' +
        'drop table if exists iut;\n' +
        'create table iut (\n' +
            'noIut int(5) not null auto_increment,\n' +
            'nomIut varchar(80),\n' +
            'adresse varchar(80),\n' +
            'nbEtudiants int(5),\n' +
            'primary key (noIut)\n' +
        ');\n\n' +
        'create table epreuve (\n' +
            'numEpreuve int(5) not null auto_increment,\n' +
            'intitule varchar(80),\n' +
            'primary key(numEpreuve)\n' +
        ');\n\n' +
        'create table manifestation (\n' +
            'numMan int(5) not null auto_increment,\n' +
            'nomMan varchar(80),\n' +
            'dateMan date,\n' +
            'noIut int(5) not null,\n' +
            'primary key (numMan),\n' +
            'foreign key (noIut) references iut(noIut)\n' +
        ');\n\n' +
        'create table etudiant (\n' +
            'noEtudiant int(5) not null auto_increment,\n' +
            'nom varchar(80),\n' +
            'age int(2),\n' +
            'sexe enum("M", "F"),\n' +
            'noIut int(5) not null,\n' +
            'primary key(noEtudiant),\n' +
            'foreign key (noIut) references iut(noIut)\n' +
        ');\n\n' +
        'create table participe (\n' +
            'numMan int(5) not null,\n' +
            'numEpreuve int(5) not null,\n' +
            'noEtudiant int(5) not null,\n' +
            'resultat int(5),\n' +
            'primary key (numMan, numEpreuve, noEtudiant),\n' +
            'foreign key (numMan) references manifestation(numMan),\n' +
            'foreign key (numEpreuve) references epreuve(numEpreuve),\n' +
            'foreign key (noEtudiant) references etudiant(noEtudiant)\n' +
        ');\n\n' +
        'create table contenu (\n' +
            'numMan int(5) not null,\n' +
            'numEpreuve int(5) not null,\n' +
            'primary key (numMan, numEpreuve),\n' +
            'foreign key (numMan) references manifestation(numMan),\n' +
            'foreign key (numEpreuve) references epreuve(numEpreuve)\n' +
        ');\n'
    );
}

